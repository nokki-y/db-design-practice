--
-- 同じカテゴリ構造を閉包テーブルモデルで表現すると
-- 以下のようになります。

CREATE TABLE categories_ct (
    category_id    CHAR(4)      NOT NULL PRIMARY KEY,
    category_name  VARCHAR(50)  NOT NULL
);

CREATE TABLE category_closure (
    ancestor    CHAR(4) NOT NULL REFERENCES categories_ct(category_id),
    descendant  CHAR(4) NOT NULL REFERENCES categories_ct(category_id),
    depth       INTEGER NOT NULL,
    PRIMARY KEY (ancestor, descendant)
);

